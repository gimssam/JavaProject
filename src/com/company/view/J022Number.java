package com.company.view;
// Number Type
public class J022Number {

	public static void main(String[] args) {
		// 연산자 Operator
		System.out.println(6 + 2);  // 8
		System.out.println(6 - 2);  // 4
		System.out.println(6 * 2);  // 12
		System.out.println(6 / 2);  // 3
		
		// 수학과 관련된 중요한 것을 그룹 해놓은 명령어를 모아놓은 캐비넷 = Math
		// 객체를 이용하면 쉽게 이용하여 여러 수학적 작업을 할수 있다.
		System.out.println(Math.PI); // 파이 
		System.out.println(Math.floor(Math.PI)); // 내림메서드 3
		System.out.println(Math.ceil(Math.PI)); // 올림메서드 4
	}

}
